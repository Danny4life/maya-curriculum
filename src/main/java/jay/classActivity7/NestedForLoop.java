package jay.classActivity7;

public class NestedForLoop {
    public static void main(String[] args) {
        for(int i = 1; i <= 3; i++){ // Outer loop
            for(int j = 1; j <= 3; j++){ // Inner loop
                System.out.println(i + " " + j);
            }
        }
    }
    // break it down in the next slide
}
