package jay.classActivity4;

public class ifElseIfElseLadder {
    public static void main(String[] args) {

        int day = 3;

        if(day == 1){
            System.out.println("MONDAY");
        } else if (day == 2) {
            System.out.println("TUESDAY");

        } else if (day == 3) {
            System.out.println("WEDNESDAY");

        }else {
            System.out.println("NOT A DAY");
        }
    }
}
