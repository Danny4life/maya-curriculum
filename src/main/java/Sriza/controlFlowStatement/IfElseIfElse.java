package Sriza.controlFlowStatement;

public class IfElseIfElse {
    public static void main(String[] args) {

        int marks = 80;
        
        if(marks >= 90){
            System.out.println("GRADE A");
        } else if (marks >= 75) {
            System.out.println("GRADE B");
            
        }else {
            System.out.println("GRADE C");
        }
    }
}
