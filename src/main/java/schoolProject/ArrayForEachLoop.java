package schoolProject;

public class ArrayForEachLoop {

    public static void main(String[] args) {

        int[] arr = {1,2,3,4,5};


        // W.O.R.A --- Write - Once -  Run - Anywhere
        // D.R.Y --- Don't - Repeat - Yourself


        for(int a : arr){
            System.out.println(a);
        }
    }
}
